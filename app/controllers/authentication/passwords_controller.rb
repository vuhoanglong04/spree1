  # POST /resource/password
  def create
    self.resource = resource_class.send_reset_password_instructions(resource_params)
    yield resource if block_given?

    if successfully_sent?(resource)
      flash[:alert] = "Please check your email to reset your password"
      redirect_to new_user_session_path, status: :see_other
    else
      flash[:alert] = resource.errors.full_messages.join("\n")
      redirect_to new_user_session_path, status: :see_other
    end
  end

  # GET /resource/password/edit?reset_password_token=abcdef
  def edit
    self.resource = resource_class.new
    set_minimum_password_length
    resource.reset_password_token = params[:reset_password_token]
  end

  # PUT /resource/password
  def update
    self.resource = resource_class.reset_password_by_token(resource_params)
    yield resource if block_given?

    if resource.errors.empty?
      resource.unlock_access! if unlockable?(resource)
      if resource_class.sign_in_after_reset_password
        flash_message = resource.active_for_authentication? ? :updated : :updated_not_active
        set_flash_message!(:notice, flash_message)
        resource.after_database_authentication
        sign_in(resource_name, resource)
      else
        set_flash_message!(:notice, :updated_not_active)
      end
      flash[:notice] = "Updated your password"
      redirect_to root_path, status: :see_other
    else
      flash[:alert] = resource.errors.full_messages.join("\n")
      redirect_to user_password_path(resource_name), status: :see_other
    end
  end

